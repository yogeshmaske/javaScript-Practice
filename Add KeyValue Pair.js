// Add Key/Value Pair
// Example 1: Add Key/Value Pair to an Object Using Dot Notation
// program to add a key/value pair to an object

const person = {
    name: 'Monica',
    age: 22,
    gender: 'female'
}

// add a key/value pair
person.height = 5.4;

console.log(person);


// Example 2: Add Key/Value Pair to an Object Using Square Bracket Notation
// program to add a key/value pair to an object

const person = {
    name: 'Monica',
    age: 22,
    gender: 'female'
}

// add a key/value pair
person['height'] = 5.4;

console.log(person);